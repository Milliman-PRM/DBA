CREATE FUNCTION merge_ranges(tstzrange[]) RETURNS SETOF tstzrange
    LANGUAGE plpgsql
    AS $_$
declare
    t tstzrange;
    r tstzrange;
begin
    foreach t in array $1 loop
        if r && t then r:= r + t;
        else
            if r notnull then return next r;
            end if;
            r:= t;
        end if;
    end loop;
    if r notnull then return next r;
    end if;
end $_$;


