CREATE VIEW view_activity_log WITH (security_barrier='true') AS
 WITH fetch_names AS (
         SELECT qvauditlog.id,
            selected_fields.field_name,
            (char_length(selected_fields.field_name) = max(char_length(selected_fields.field_name)) OVER (PARTITION BY qvauditlog.id ROWS BETWEEN UNBOUNDED PRECEDING AND UNBOUNDED FOLLOWING)) AS longest_name
           FROM (qvauditlog
             JOIN selected_fields ON ((qvauditlog.message ~~ (selected_fields.field_name || '%'::text))))
          WHERE ((qvauditlog.eventtype)::text = ANY ((ARRAY['Selection'::character varying, 'Bookmark Selection'::character varying])::text[]))
        ), sessions AS (
         SELECT view_session_log.sessionid,
            view_session_log.userid,
            view_session_log.groupid,
            view_session_log.reportid,
            view_session_log.session_start_time,
            view_session_log.session_duration
           FROM view_session_log
        )
 SELECT sessions.sessionid,
    log.useraccessdatetime AS actiondatetime,
    log.eventtype AS event_type,
        CASE
            WHEN (((log.eventtype)::text = ANY ((ARRAY['Selection'::character varying, 'Bookmark Selection'::character varying])::text[])) AND (fetch_names.field_name IS NOT NULL)) THEN fetch_names.field_name
            WHEN (((log.eventtype)::text = ANY ((ARRAY['Selection'::character varying, 'Bookmark Selection'::character varying])::text[])) AND (log.message <> 'Clear All'::text)) THEN 'Unmatched Field'::text
            ELSE log.message
        END AS event_message
   FROM ((qvauditlog log
     LEFT JOIN fetch_names ON (((log.id = fetch_names.id) AND (fetch_names.longest_name = true))))
     JOIN sessions ON (((log.fk_user_id = sessions.userid) AND (log.fk_group_id = sessions.groupid) AND (log.fk_report_id = sessions.reportid) AND (log.useraccessdatetime >= sessions.session_start_time) AND (log.useraccessdatetime <= (sessions.session_start_time + sessions.session_duration)))));


